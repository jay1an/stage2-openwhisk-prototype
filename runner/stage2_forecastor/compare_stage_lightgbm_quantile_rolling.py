POLICIES = {"p50": 0.50, "p90": 0.90, "p95": 0.95}


@dataclass
class QuantileModelSet:
    models: dict[str, object]
    fallback_quantiles: dict[str, float]
    shifts: dict[str, float]
    features: list[str]
    calibration_mode: str


